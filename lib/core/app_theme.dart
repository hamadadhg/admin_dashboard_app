  /// Helper method to get monospace font for data display (reservation IDs, numbers)
  static TextStyle getMonospaceStyle({
    required bool isLight,
    double fontSize = 14,
    FontWeight fontWeight = FontWeight.w400,
  }) {
    final Color textColor = isLight ? textPrimaryLight : textPrimaryDark;
    return GoogleFonts.robotoMono(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: textColor,
      letterSpacing: 0,
    );
  }

  /// Helper method to get status-specific colors
  static Color getStatusColor(String status, {bool isLight = true}) {
    switch (status.toLowerCase()) {
      case 'confirmed':
      case 'seated':
        return successLight;
      case 'pending':
      case 'waiting':
        return warningLight;
      case 'cancelled':
      case 'no-show':
        return errorLight;
      default:
        return isLight ? textSecondaryLight : textSecondaryDark;
    }
  }
